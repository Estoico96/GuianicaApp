package com.ninjabyte.guianica.model;

public class Commercial {
    private String url;
    private String name;
    private String description;
    private String number;

    public Commercial() {
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getNumber() {
        return number;
    }

    public String getUrl() {
        return url;
    }
}
